
# циклите са 2 типа : 
#   while цикли:
#       Състоят се от проверка и се изпълняват докато тази проверка се удовлетворява(true)
#   for цикли:
#       Използват се за итериране през някаква редица от елементи (масиви, редици, листи)
#       засега ще го ползваме за изпълняване на цикъля определен брой пъти


